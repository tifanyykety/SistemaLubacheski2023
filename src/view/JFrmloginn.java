    private void tksl_jBtnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tksl_jBtnLoginActionPerformed
        TkslUsuarios usuarios = new TkslUsuarios();
        tksl_UsuarioDao usuarioDao = new tksl_UsuarioDao();
        usuarios = (TkslUsuarios) usuarioDao.login(tksl_jTxtUsuario.getText(), tksl_jPswSenha.getText());

        if (tksl_jPswSenha.getText().equals("senha123") && tksl_jTxtUsuario.getText().equals("Tifany")
                || (tksl_jPswSenha.getText().equals("senha123") && !tksl_jTxtUsuario.getText().equals("Katy"))) {
            JFrmPrincipal jFrmPrincipal = new JFrmPrincipal();
            jFrmPrincipal.setVisible(true);
            this.dispose();//mesma coisa do setvisible
            util.mensagem("Dados corretos");

        } else if (usuarios == null) {
            util.mensagem("Dados incorretos");
        } else {
            JFrmPrincipal jFrmPrincipal = new JFrmPrincipal();
            jFrmPrincipal.setVisible(true);
            this.dispose();//mesma coisa do setvisible
            util.mensagem("Dados corretos");
        }


    }//GEN-LAST:event_tksl_jBtnLoginActionPerformed

