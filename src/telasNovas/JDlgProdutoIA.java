    public TkslProduto viewBean() {
        int id = Integer.valueOf(tksl_jTxtId.getText());
        produto.setTkslIdProduto(id);
        produto.setTkslNome(tksl_jTxtNome.getText());
        double valor = Double.parseDouble(tksl_jTxtValorUnitario.getText());
        produto.setTkslValorUnitario(valor);
        produto.setTkslNumero(tksl_jTxtNumero.getText());
        produto.setTkslTamanho(tksl_jTxtTamanho.getText());
        produto.setTkslTime(tksl_jTxtTime.getText());
        produto.setTkslCor(tksl_jTxtCor.getText());

        return produto;
    }

    public void beanView(TkslProduto produto) {
        tksl_jTxtId.setText(String.valueOf(produto.getTkslIdProduto()));
        tksl_jTxtCor.setText(produto.getTkslCor());
        tksl_jTxtNumero.setText(produto.getTkslNumero());
        tksl_jTxtNome.setText(produto.getTkslNome());
        tksl_jTxtTamanho.setText(produto.getTkslTamanho());
        tksl_jTxtTime.setText(produto.getTkslTime());
        tksl_jFmtModeloAno.setText(dateFormat.format(produto.getTkslModeloAno()));
        tksl_jTxtValorUnitario.setText(String.valueOf(produto.getTkslValorUnitario()));
    }

