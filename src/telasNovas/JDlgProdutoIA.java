    private void tksl_jBtnConfirmar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tksl_jBtnConfirmar1ActionPerformed
        // TODO add your handling code here:
        if (getTitle().equals("Incluindo")) {
            produto = viewBean();
            produtoDao.insert(produto);
            util.mensagem("Incluindo");
            setVisible(false);
        } else if (getTitle().equals("Alterando")) {
            produtoDao.update(produto);
            tksl_jTxtId.setEnabled(false);
            util.mensagem("Alterado");
            setVisible(false);
        } else {
            util.mensagem("Impossivel de se realizar a operação");
        }


    }//GEN-LAST:event_tksl_jBtnConfirmar1ActionPerformed

    private void tksl_jBtnCancelar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tksl_jBtnCancelar1ActionPerformed
        // TODO add your handling code here:
        if (util.perguntar("Quer cancelar")) {
            limparCampos();
            setVisible(false);
        } else {
            util.mensagem("Operação continua");
        }
    }//GEN-LAST:event_tksl_jBtnCancelar1ActionPerformed

